public class SmallerNumber {
    public static void main(String[] args) {
        // Define two numbers
        int num1 = 25;
        int num2 = 15;
        
        // Compare and find the smaller number
        if (num1 < num2) {
            System.out.println("The smaller number is: " + num1);
        } else {
            System.out.println("The smaller number is: " + num2);
        }
    }
}
